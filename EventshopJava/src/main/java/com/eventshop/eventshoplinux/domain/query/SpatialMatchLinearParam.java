package com.eventshop.eventshoplinux.domain.query;

public class SpatialMatchLinearParam {

	String startX;
	String startY;
	String startValue;
	String dirGradient;
	String valGradient;

	public String getStartX() {
		return startX;
	}

	public void setStartX(String startX) {
		this.startX = startX;
	}

	public String getStartY() {
		return startY;
	}

	public void setStartY(String startY) {
		this.startY = startY;
	}

	public String getStartValue() {
		return startValue;
	}

	public void setStartValue(String startValue) {
		this.startValue = startValue;
	}

	public String getDirGradient() {
		return dirGradient;
	}

	public void setDirGradient(String dirGradient) {
		this.dirGradient = dirGradient;
	}

	public String getValGradient() {
		return valGradient;
	}

	public void setValGradient(String valGradient) {
		this.valGradient = valGradient;
	}

}
